public class S2 {
    public static void main(String[] args) {
        for (int i = 100; i >= 1; i--) {
            System.out.print(i + ", ");
        }
        System.out.println();
        int i = 100;
        while (i >= 1){
            System.out.print(i + "; ");
            i--;
        }
    }
}
